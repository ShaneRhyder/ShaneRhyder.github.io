function displayAlbums() {
    var cardContainer = document.getElementById("card-container");

    for (var i = 1; i <= Object.keys(albums).length; i++) {
        var key = "album" + i;
        var albumArray = albums[key];

        cardContainer.innerHTML += `
            <div class="col-12 col-md-4 my-2">
                <div class="card shadow-light">
                    <img src="${albumArray.cover}" class="card-img-top"
                        alt="...">
                    <div class="card-body">
                        <h5 class="card-title fw-bold text-truncate">${albumArray.title}</h5>
                        <p><span class="fw-bold">Artist: </span>${albumArray.artist}</p>
                        <a href="#album-section" class="btn btn-warning fw-bold"
                            onclick="loadAlbum('album${i}')"></button>View Album</a>
                    </div>
                </div>
            </div>
            `;
    }
}

function loadAlbum(albumKey) {
    var album = albums[albumKey];
    var section = document.getElementById('album-section');

    section.innerHTML = `
        <div class="row">
          <div class="d-flex align-items-center justify-content-center col-12 col-md-4 text-center mb-4 mb-md-0">
            <a href="${album.link}"><img src="${album.cover}" alt="${album.title} Cover" class="img-fluid rounded shadow album-cover"></a>
          </div>
          <div class="col-12 col-md-8">
            <a href="${album.link}" class="text-decoration-none"><h2 class="fw-bold text-primary">${album.title}</h2></a>
            <h5 class="fw-bold text-primary">- ${album.artist}</h5>
            <p class="mt-3 text-dark">${album.description}</p>
            <p class="m-0 text-dark"><span class="fw-bold text-primary">Released:</span> ${album.releaseDate}</p>
            <p class="m-0 text-dark"><span class="fw-bold text-primary">Recorded:</span> ${album.recordedDate}</p>
          </div>
        </div>

        <div class="my-5 bg-dark p-3 rounded-3">
          <h4 class="fw-bold text-warning mb-3">Tracklist</h4>
          <div id="tracklist-container"></div>
        </div>

        <div class="text-center h4 fw-bold text-primary">
        <p>Curious what day this album was released? Find out now with this cool tool!</p>
        <p class="text-dark">(Or use it for anything else, actually. :P)</p>
        </div>
        <div class="d-flex justify-content-center">
        <iframe class="mt-3 rounded-3 bg-warning" src="https://shanerhyder.github.io/ADET/A07/embeddable.html" style="width: 50%; height: 33dvh">
        </iframe>
        </div>
      `;

    var tracklistContainer = document.getElementById('tracklist-container');
    album.tracks.forEach(trackID => {
        tracklistContainer.innerHTML += `
                <iframe class="mb-2 track" style="border-radius:12px" src="https://open.spotify.com/embed/track/${trackID}" width="100%" height="80" frameborder="0"
                allow="autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture" loading="eager">
                </iframe>
                `
    });

    var albumSection = document.getElementById("album-section");

    albumSection.classList.add("py-5");
}

